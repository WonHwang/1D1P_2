'''
풀이

g(7)
= f(1) + f(2) + ... + f(7)
= (1) + (1 + 2) + (1 + 3) + (1 + 2 + 4) + (1 + 5) + (1 + 2 + 3 + 6) + (1 + 7)
= (1 * 7) + (2 * 3) + (3 * 2) + (4 * 1) + (5 * 1) + (6 * 1) + (7 * 1) -> 1 to n 까지 n으로 나눈 몫 개수 만큼 등장
'''

answer = 0
N = int(input())
for i in range(1, N+1):
    answer += (N//i) * i
print(answer)